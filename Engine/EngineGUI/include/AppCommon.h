#ifndef APPCOMMON_H
#define APPCOMMON_H

#include <sl/Camera.hpp>
#include <opencv2/opencv.hpp>

std::vector<sl::uint2> cvt(const cv::Rect &bbox_in){
    std::vector<sl::uint2> bbox_out(4);
    bbox_out[0] = sl::uint2(bbox_in.x, bbox_in.y);
    bbox_out[1] = sl::uint2(bbox_in.x + bbox_in.width, bbox_in.y);
    bbox_out[2] = sl::uint2(bbox_in.x + bbox_in.width, bbox_in.y + bbox_in.height);
    bbox_out[3] = sl::uint2(bbox_in.x, bbox_in.y + bbox_in.height);
    return bbox_out;
}

cv::Mat QCameraCapture::slMat2cvMat(sl::Mat &input)
{
    return cv::Mat(input.getHeight(),
                   input.getWidth(),
                   getOpenCVType(input.getDataType()),
                   input.getPtr<sl::uchar1>(sl::MEM::CPU),
                   input.getStepBytes(sl::MEM::CPU));
}

int QCameraCapture::getOpenCVType(sl::MAT_TYPE type)
{
    int cv_type = -1;
    switch (type) {
        case sl::MAT_TYPE::F32_C1: cv_type = CV_32FC1; break;
        case sl::MAT_TYPE::F32_C2: cv_type = CV_32FC2; break;
        case sl::MAT_TYPE::F32_C3: cv_type = CV_32FC3; break;
        case sl::MAT_TYPE::F32_C4: cv_type = CV_32FC4; break;
        case sl::MAT_TYPE::U8_C1: cv_type = CV_8UC1; break;
        case sl::MAT_TYPE::U8_C2: cv_type = CV_8UC2; break;
        case sl::MAT_TYPE::U8_C3: cv_type = CV_8UC3; break;
        case sl::MAT_TYPE::U8_C4: cv_type = CV_8UC4; break;
        default: break;
    }
    return cv_type;
}

#endif // APPCOMMON_H
